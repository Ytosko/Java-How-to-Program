// Example. 5.2: ForCounter
 
package chapter_05;

/**
 *
 * @author Ytosko
 */
public class ForCounter {
    
    public static void main( String[] args ) {
        
        for( int counter = 1; counter <= 10; counter++ ) {
            System.out.printf("%d ", counter);
        }
        System.out.println();
    }
    
}
